.orig x3000
	LD R1, NUM
	LD R6, PTR
	ADD R5, R1, #0
	JSRR R6

	HALT

;data 
NUM .fill #-230
PTR .fill x3200

.orig x3200
;sub: shifter 
;INPUT: R1 CONTAINS THE VALUE YOUR SHIFTING
;POST: R6 WILL CONTAIN THE VALUE OF THE SHIFT

	ST R7, R7_BACKUP
	LD R2, SENT
	ADD R5, R5, #0
	BRzp LOOP
	NOT R1, R1
	ADD R1, R1, #1

LOOP
	ADD R1, R1, #0
	BRn NEG
	BR POS

POS
	ADD R1, R1, R1
	BR CHECK
NEG
	ADD R1, R1, R1
	ADD R1, R1, #1
CHECK
	ADD R2, R2, #-1
	BRp LOOP
	
	ADD R5, R5, #0
	BRn CONVERT
END
	HALT

CONVERT
	NOT R1, R1
	ADD R1, R1, #1
	BR END

;data
SENT .FILL #15
R7_BACKUP .BLKW #1

.END

